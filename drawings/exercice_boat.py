import turtle

turtle.speed(3)

turtle.pendown()
turtle.forward(200)
turtle.left(120)
turtle.forward(200)
turtle.left(120)
turtle.forward(200)
turtle.penup()
turtle.backward(200)
turtle.left(30)
turtle.pendown()
turtle.goto(100, 0)
turtle.penup()

turtle.goto(0, -10)
turtle.left(90)
turtle.pendown()
turtle.forward(200)
turtle.right(150)
turtle.forward(50)
turtle.right(30)
turtle.forward(120)
turtle.right(30)
turtle.forward(50)
turtle.penup()


turtle.done()
